#==============================================================================#
# MNA Phase 1: Basic Device Stamps
#
# This module provides stamp! methods for basic circuit elements.
# Each device type has a corresponding stamp! method that adds its
# contribution to the MNA matrices (G, C) and RHS vector (b).
#
# Devices implemented:
# - Resistor: stamps conductance G = 1/R
# - Capacitor: stamps capacitance C
# - Inductor: stamps inductance L (with current variable)
# - VoltageSource: stamps voltage constraint (with current variable)
# - CurrentSource: stamps source current into RHS
# - VCVS, VCCS: voltage-controlled sources
#==============================================================================#

using StaticArrays: SVector

#==============================================================================#
# PWL Interpolation (from Cadnip.spectre_env.jl)
# Included here because MNA is loaded before spectre_env.jl
#==============================================================================#

"""
    find_t_in_ts(ts, t) -> Int

Find the index in sorted array `ts` where `t` would be inserted.
Uses searchsortedfirst for O(log n) lookup on SVector.
"""
@inline function find_t_in_ts(ts, t)
    idx = Base.searchsortedfirst(ts, t)
    if idx <= length(ts) && ts[idx] == t
        return idx + 1
    end
    return idx
end

"""
    pwl_at_time(ts, ys, t) -> value

Evaluate piecewise-linear function at time t.
Works with SVector (uses searchsortedfirst for O(log n) lookup).

Handles edge cases: before first point, after last point, flat segments,
and infinitely steep segments (same time, different values).
"""
@inline function pwl_at_time(ts, ys, t)
    i = find_t_in_ts(ts, t)
    type_stable_time = 0.0 * t  # Preserves type (e.g., ForwardDiff.Dual)
    if i <= 1
        # Signal is before the first timepoint, hold the first value.
        return ys[1] + type_stable_time
    end
    if i > length(ts)
        # Signal is beyond the final timepoint, hold the final value.
        return ys[end] + type_stable_time
    end
    begin
        if ys[i-1] == ys[i]
            # signal is constant/flat (singularity in y)
            return ys[i] + type_stable_time
        end
        if ts[i] == ts[i-1]
            # signal is infinitely steep (singularity in t)
            return (ys[i-1] + ys[i])/2 + type_stable_time
        end
        # The general case: linear interpolation
        slope = (ys[i] - ys[i-1])/(ts[i] - ts[i-1])
        return ys[i-1] + (t - ts[i-1])*slope
    end
end

"""
    pulse_at_time(v1, v2, td, tr, tf, pw, per, t) -> value

Evaluate a SPICE PULSE source at time `t`.

- Before the delay `td`, holds the initial value `v1`.
- From `td` onward the waveform repeats with period `per`: a linear rise from
  `v1` to `v2` over `tr`, a flat top at `v2` for `pw`, a linear fall back to
  `v1` over `tf`, then a flat bottom at `v1` for the rest of the period.

A non-positive `per` produces a single (non-repeating) pulse.
"""
@inline function pulse_at_time(v1, v2, td, tr, tf, pw, per, t)
    type_stable_time = 0.0 * t  # Preserves type (e.g. ForwardDiff.Dual)
    if t < td
        return v1 + type_stable_time
    end
    # Phase within the current period; a single pulse when per <= 0.
    phase = per > 0 ? mod(t - td, per) : (t - td)
    if phase < tr
        # Rising edge (instantaneous if tr == 0)
        return tr > 0 ? v1 + (v2 - v1) * (phase / tr) : v2 + type_stable_time
    elseif phase < tr + pw
        return v2 + type_stable_time
    elseif phase < tr + pw + tf
        # Falling edge (instantaneous if tf == 0)
        return tf > 0 ? v2 + (v1 - v2) * ((phase - tr - pw) / tf) : v1 + type_stable_time
    else
        return v1 + type_stable_time
    end
end

export stamp!
export Resistor, Capacitor, Inductor
export VoltageSource, CurrentSource
export VCVS, VCCS, CCVS, CCCS
export pwl_at_time, pulse_at_time

#==============================================================================#
# Wave Callable Structs
#
# Callable replacements for the anonymous `tran = _t -> ...` closures
# generated by spc/codegen.jl. Being concrete structs (rather than closures)
# makes their breakpoint times enumerable from the constructed object at
# build time via `breakpoints(wave)` (see breakpoints.jl) - params may be
# runtime expressions, so parse-time extraction from the netlist would be
# wrong.
#==============================================================================#

"""
    PWLWave(ts, ys)

Callable piecewise-linear waveform: `PWLWave(ts, ys)(t) == pwl_at_time(ts, ys, t)`.

`ts`/`ys` are either both `SVector{N,Float64}` (zero-allocation path, compile-time
constant vertex count) or both `Vector{Float64}` (dynamic fallback).
"""
struct PWLWave{TS,YS} <: Wave
    ts::TS
    ys::YS
end

@inline (w::PWLWave)(t::Real) = pwl_at_time(w.ts, w.ys, t)

"""
Breakpoints at every PWL vertex time - the integrator should land exactly on
each vertex to resolve the kink in slope there.

`Vector{Float64}(ts)` rather than `collect`/comprehension: StaticArrays makes
those return a `SizedVector` for `SVector` input, while the Array constructor
always yields a plain (freshly copied) `Vector{Float64}`.
"""
breakpoints(w::PWLWave) = BreakpointSpec(Vector{Float64}(w.ts))

export PWLWave

"""
    SinWave(vo, va, freq, td, theta, phase)

Callable SPICE SIN source: constant `vo + va*sind(phase)` before `td`, then
`vo + va*exp(-theta*(t-td))*sind(360*freq*(t-td) + phase)`.
"""
struct SinWave <: Wave
    vo::Float64
    va::Float64
    freq::Float64
    td::Float64
    theta::Float64
    phase::Float64
end

function SinWave(vo::Real, va::Real, freq::Real, td::Real=0.0, theta::Real=0.0, phase::Real=0.0)
    SinWave(Float64(vo), Float64(va), Float64(freq), Float64(td), Float64(theta), Float64(phase))
end

@inline function (w::SinWave)(t::Real)
    if t < w.td
        w.vo + w.va * sind(w.phase)
    else
        w.vo + w.va * exp(-w.theta * (t - w.td)) * sind(360 * w.freq * (t - w.td) + w.phase)
    end
end

"""
The onset at `td` is a slope discontinuity (value is continuous, derivative
jumps from 0 to the sinusoid's slope) - a breakpoint hint when `td > 0`.
"""
breakpoints(w::SinWave) = w.td > 0 ? BreakpointSpec(Float64[w.td]) : nothing

export SinWave

"""
    PulseWave(v1, v2, td, tr, tf, pw, per)

Callable SPICE PULSE source: see `pulse_at_time` for the waveform definition.
"""
struct PulseWave <: Wave
    v1::Float64
    v2::Float64
    td::Float64
    tr::Float64
    tf::Float64
    pw::Float64
    per::Float64
end

function PulseWave(v1::Real, v2::Real, td::Real, tr::Real, tf::Real, pw::Real, per::Real)
    PulseWave(Float64(v1), Float64(v2), Float64(td), Float64(tr), Float64(tf), Float64(pw), Float64(per))
end

@inline (w::PulseWave)(t::Real) = pulse_at_time(w.v1, w.v2, w.td, w.tr, w.tf, w.pw, w.per, t)

"""
Breakpoints at each edge (rise start, flat-top start, fall start, flat-bottom
start) within the first period, repeated every `per` seconds when `per > 0`
(a non-positive `per` is a single non-repeating pulse, matching
`pulse_at_time`).
"""
function breakpoints(w::PulseWave)
    edges = Float64[w.td, w.td + w.tr, w.td + w.tr + w.pw, w.td + w.tr + w.pw + w.tf]
    return w.per > 0 ? BreakpointSpec(edges, w.per, -1) : BreakpointSpec(edges)
end

export PulseWave

#==============================================================================#
# Device Type Hierarchy
#
# Abstract types for device categorization:
# - AbstractMNADevice: base type for all devices
# - AbstractVoltageSource: devices that create a current variable (for KVL constraint)
# - AbstractCurrentSource: devices that inject current but don't create variables
#
# Key distinction: Voltage sources create current variables that can be referenced
# by controlled sources (CCVS, CCCS). Current sources only modify the RHS vector.
#==============================================================================#

"""Base type for all MNA circuit devices."""
abstract type AbstractMNADevice end

"""
Devices that impose a voltage constraint and create a current variable.

All voltage sources (independent, controlled, behavioral) create a current variable
in the MNA system. This current can be referenced by CCVS/CCCS controlled sources.
"""
abstract type AbstractVoltageSource <: AbstractMNADevice end

"""
Devices that inject/absorb current without creating additional variables.

Current sources only modify the RHS vector `b`, they don't add variables to the system.
"""
abstract type AbstractCurrentSource <: AbstractMNADevice end

"""Passive devices (resistors, capacitors, inductors)."""
abstract type AbstractPassiveDevice <: AbstractMNADevice end

export AbstractMNADevice, AbstractVoltageSource, AbstractCurrentSource, AbstractPassiveDevice

