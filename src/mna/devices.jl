"""
    stamp!(device, ctx::AnyMNAContext, ports...)

Stamp a device into the MNA context.

The number and meaning of ports depends on the device type:
- 2-terminal devices (R, C, L, V, I): stamp!(dev, ctx, p, n)
- 4-terminal sources (VCVS, VCCS): stamp!(dev, ctx, out_p, out_n, in_p, in_n)
"""
function stamp! end

#------------------------------------------------------------------------------#
# Resistor: G matrix only
#------------------------------------------------------------------------------#

"""
    stamp!(R::Resistor, ctx::AnyMNAContext, p::Int, n::Int)

Stamp a resistor between nodes p and n.

The resistor contributes I = (Vp - Vn)/R to KCL at each node.
Using "current leaving node is positive" convention:
- KCL at p: +G*Vp - G*Vn (current leaves p)
- KCL at n: -G*Vp + G*Vn (current enters n)

Matrix pattern (for G = 1/R):
```
     Vp  Vn
Vp [ +G  -G ]
Vn [ -G  +G ]
```
"""
function stamp!(R::Resistor, ctx::AnyMNAContext, p::Int, n::Int)
    G = 1.0 / R.r
    stamp_conductance!(ctx, p, n, G)
    return nothing
end

#------------------------------------------------------------------------------#
# Capacitor: C matrix only
#------------------------------------------------------------------------------#

"""
    stamp!(C::Capacitor, ctx::AnyMNAContext, p::Int, n::Int)

Stamp a capacitor between nodes p and n.

The capacitor contributes I = C * d(Vp - Vn)/dt to KCL.
This stamps into the C matrix with the same pattern as a resistor.

Matrix pattern:
```
     Vp  Vn
Vp [ +C  -C ]
Vn [ -C  +C ]
```
"""
function stamp!(C::Capacitor, ctx::AnyMNAContext, p::Int, n::Int)
    stamp_capacitance!(ctx, p, n, C.c)
    return nothing
end

#------------------------------------------------------------------------------#
# Inductor: G and C matrices, needs current variable
#------------------------------------------------------------------------------#

"""
    stamp!(L::Inductor, ctx::AnyMNAContext, p::Int, n::Int) -> Int

Stamp an inductor between nodes p and n.
Returns the index of the inductor current variable.

The inductor uses a current variable I_L because current is a state variable.
Equations:
1. KCL at p: current I_L leaves p
2. KCL at n: current I_L enters n
3. Voltage equation: V_p - V_n = L * dI_L/dt

Matrix pattern:
```
        Vp  Vn  I_L
    Vp [ .   .  +1  ]     KCL: I_L leaves p
    Vn [ .   .  -1  ]     KCL: I_L enters n
G = I_L[+1  -1   .  ]     Voltage: Vp - Vn

        Vp  Vn  I_L
    Vp [ .   .   .  ]
    Vn [ .   .   .  ]
C = I_L[ .   .  -L  ]     Voltage: ... = L * dI/dt
```

Note: The -L in C comes from rewriting V = L*dI/dt as:
      G*x + C*dx/dt = b
      Vp - Vn - L*dI/dt = 0
"""
function stamp!(L::Inductor, ctx::AnyMNAContext, p::Int, n::Int)
    # Allocate current variable using component-based API (zero allocation for DirectStampContext)
    I_idx = alloc_current!(ctx, :I_, L.name)

    # KCL: current I_L flows from p to n
    stamp_G!(ctx, p, I_idx,  1.0)
    stamp_G!(ctx, n, I_idx, -1.0)

    # Voltage equation: Vp - Vn - L*dI/dt = 0
    # G part: Vp - Vn
    stamp_G!(ctx, I_idx, p,  1.0)
    stamp_G!(ctx, I_idx, n, -1.0)

    # C part: -L * dI/dt
    stamp_C!(ctx, I_idx, I_idx, -L.l)

    return I_idx
end

#------------------------------------------------------------------------------#
# Voltage Source: G matrix and b vector, needs current variable
#------------------------------------------------------------------------------#

"""
    stamp!(V::VoltageSource, ctx::AnyMNAContext, p::Int, n::Int) -> Int

Stamp a voltage source between nodes p and n (V_p - V_n = V.dc).
Returns the index of the source current variable.

This is the simple DC stamp - for time-dependent analysis use the
version with time and mode arguments.

The voltage source needs a current variable because current is unknown.
Equations:
1. KCL at p: current I_V leaves p (enters source at + terminal)
2. KCL at n: current I_V enters n
3. Voltage equation: V_p - V_n = V_dc

Matrix pattern:
```
        Vp  Vn  I_V
    Vp [ .   .  +1  ]     KCL: I_V leaves p
    Vn [ .   .  -1  ]     KCL: I_V enters n
G = I_V[+1  -1   .  ]     Voltage constraint: Vp - Vn

    I_V
b = V_dc                  Source voltage value (DC)
b_ac = V_ac               Source voltage value (AC small-signal)
```
"""
function stamp!(V::VoltageSource, ctx::AnyMNAContext, p::Int, n::Int)
    # Allocate current variable using component-based API (zero allocation for DirectStampContext)
    I_idx = alloc_current!(ctx, :I_, V.name)

    # KCL: current I_V flows from p through source to n
    stamp_G!(ctx, p, I_idx,  1.0)
    stamp_G!(ctx, n, I_idx, -1.0)

    # Voltage equation: Vp - Vn = V_dc
    stamp_G!(ctx, I_idx, p,  1.0)
    stamp_G!(ctx, I_idx, n, -1.0)
    stamp_b!(ctx, I_idx, V.dc)

    return I_idx
end

"""
    stamp!(V::VoltageSource, ctx::AnyMNAContext, p::Int, n::Int, t::Real, mode::Symbol) -> Int

Stamp a voltage source with time and mode awareness.

For transient analysis, evaluates the source at time t.
For DC/AC analysis, uses the DC value.
"""
@inline function stamp!(V::VoltageSource, ctx::AnyMNAContext, p::Int, n::Int,
                        t::Real, mode::Symbol)
    # Component-based API for zero allocation in DirectStampContext
    I_idx = alloc_current!(ctx, :I_, V.name)
    register_breakpoints!(ctx, V.tran)

    stamp_G!(ctx, p, I_idx,  1.0)
    stamp_G!(ctx, n, I_idx, -1.0)
    stamp_G!(ctx, I_idx, p,  1.0)
    stamp_G!(ctx, I_idx, n, -1.0)

    v = get_source_value(V, t, mode)
    stamp_b!(ctx, I_idx, v)

    # AC excitation (for small-signal analysis)
    if V.ac != 0
        stamp_b_ac!(ctx, I_idx, V.ac)
    end

    return I_idx
end

#------------------------------------------------------------------------------#
# Current Source: b vector only
#------------------------------------------------------------------------------#

"""
    stamp!(I::CurrentSource, ctx::AnyMNAContext, p::Int, n::Int)

Stamp a current source between nodes p and n.
Positive current flows from n to p (into the positive terminal p).

This is the simple DC stamp - for time-dependent analysis use the
version with time and mode arguments.

KCL contributions:
- At p: current I.dc enters (positive term)
- At n: current I.dc leaves (negative term)

This follows the passive sign convention where current enters
the positive terminal.

RHS pattern:
```
    p
b = +I      (DC current enters p)
    n
    -I      (DC current leaves n)

    p
b_ac = +I_ac   (AC current enters p)
    n
       -I_ac   (AC current leaves n)
```
"""
function stamp!(I::CurrentSource, ctx::AnyMNAContext, p::Int, n::Int)
    # Current flows from n to p (into positive terminal)
    # KCL at p: current I enters (add to RHS)
    # KCL at n: current I leaves (subtract from RHS)
    stamp_b!(ctx, p,  I.dc)
    stamp_b!(ctx, n, -I.dc)
    return nothing
end

"""
    stamp!(I::CurrentSource, ctx::AnyMNAContext, p::Int, n::Int, t::Real, mode::Symbol)

Stamp a current source with time and mode awareness.

For transient analysis, evaluates the source at time t.
For DC/AC analysis, uses the DC value.
"""
@inline function stamp!(I::CurrentSource, ctx::AnyMNAContext, p::Int, n::Int,
                        t::Real, mode::Symbol)
    register_breakpoints!(ctx, I.tran)
    i = get_source_value(I, t, mode)
    stamp_b!(ctx, p,  i)
    stamp_b!(ctx, n, -i)

    # AC excitation (for small-signal analysis)
    if I.ac != 0
        stamp_b_ac!(ctx, p,  I.ac)
        stamp_b_ac!(ctx, n, -I.ac)
    end

    return nothing
end

#------------------------------------------------------------------------------#
# VCVS: Voltage-Controlled Voltage Source
#------------------------------------------------------------------------------#

"""
    stamp!(E::VCVS, ctx::AnyMNAContext, out_p::Int, out_n::Int, in_p::Int, in_n::Int) -> Int

Stamp a voltage-controlled voltage source.
V(out_p, out_n) = E.gain * V(in_p, in_n)

Returns the index of the output current variable.

Matrix pattern:
```
            out_p out_n in_p in_n I_E
out_p      [  .     .    .    .   +1 ]  KCL at out_p
out_n      [  .     .    .    .   -1 ]  KCL at out_n
G = I_E    [ +1    -1   -A   +A    . ]  Voltage: Vout - A*Vin = 0
```
where A = E.gain
"""
function stamp!(E::VCVS, ctx::AnyMNAContext, out_p::Int, out_n::Int, in_p::Int, in_n::Int)
    # Allocate current variable for output branch (zero allocation for DirectStampContext)
    I_idx = alloc_current!(ctx, :I_, E.name)

    # KCL at output nodes
    stamp_G!(ctx, out_p, I_idx,  1.0)
    stamp_G!(ctx, out_n, I_idx, -1.0)

    # Voltage equation: V(out_p) - V(out_n) - gain * (V(in_p) - V(in_n)) = 0
    stamp_G!(ctx, I_idx, out_p,  1.0)
    stamp_G!(ctx, I_idx, out_n, -1.0)
    stamp_G!(ctx, I_idx, in_p,  -E.gain)
    stamp_G!(ctx, I_idx, in_n,   E.gain)

    return I_idx
end

